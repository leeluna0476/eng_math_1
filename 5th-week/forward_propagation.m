% 입력층
x1 = [1; 2; 1; 3];
W1 = [.1 .2 .1 .2; .3 .4 .3 .4; .5 .6 .5 .6];

% 은닉층
z1 = W1 * x1;
y1 = sigmoid(z1);

W2 = [.1 .3 .7; .2 .4 .2; .5 .5 .3; .7 .6 .1];
z2 = W2 * y1;
y2 = sigmoid(z2);

% 출력층
W3 = [.7 .5 .1 .3; .8 .6 .2 .4];
z3 = W3 * y2;
y3 = sigmoid(z3)

function y = sigmoid(z)
    y = 1 ./ (1 + exp(-z));
end